#include <stdio.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int		main()
{
	char lol[10] = "halelouya";

	printf("%d\n", ft_strlen(lol));
	return (0);
}
